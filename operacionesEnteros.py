def suma(x,y):
    resultado = int(x) + int(y)
    return resultado

def resta(x,y):
    resultado = int(x) - int(y)
    return resultado

def multiplicacion(x,y):
    resultado = int(x) * int(y)
    return resultado

def division(x,y):
    resultado = int(x)/int(y)
    return resultado